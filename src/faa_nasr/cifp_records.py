"""CIFP ARINC 424 field definitions for every section/subsection the FAA publishes."""

from __future__ import annotations

from collections import Counter


def resolve_dups(fields: list[tuple[str, int]]) -> list[tuple[str, int]]:
    """Auto-number duplicate field names: first occurrence → Name_1, second → Name_2, etc."""
    counts: Counter[str] = Counter(name for name, _ in fields)
    # Only rename names that appear more than once.
    seen: Counter[str] = Counter()
    result: list[tuple[str, int]] = []
    for name, width in fields:
        if counts[name] > 1:
            seen[name] += 1
            result.append((f"{name}_{seen[name]}", width))
        else:
            result.append((name, width))
    return result


