def crn_pos(sc: str, ssc_key: str) -> int | None:
    """Return the byte offset of ContinuationRecordNumber for a section, or None."""
    fields = PRIMARY_SPECS.get((sc, ssc_key))
    if fields is None:
        return None
    pos = 0
    for name, width in fields:
        if name == "ContinuationRecordNumber":
            return pos
        pos += width
    return None
